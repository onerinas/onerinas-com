#!/usr/bin/env node
/**
 * Generate src/_redirects from data/articles.yml.
 *
 * Each entry: /articles/{id} → /articles/{id}-{slug}/ 301
 *
 * Run: npm run redirects
 * Called automatically before build/dev.
 */

const fs = require("fs");
const path = require("path");
const yaml = require("js-yaml");

const ROOT = path.join(__dirname, "..");
const MANIFEST = path.join(ROOT, "data", "articles.yml");
const OUTPUT = path.join(ROOT, "src", "_redirects");

function canonicalPath(id, slug) {
  return `/articles/${id}-${slug}/`;
}

function main() {
  const raw = fs.readFileSync(MANIFEST, "utf8");
  const articles = yaml.load(raw);

  if (!Array.isArray(articles)) {
    throw new Error("data/articles.yml must be a list of articles");
  }

  const lines = [
    "# Generated by scripts/generate-redirects.js — do not edit by hand.",
    "# Re-run: npm run redirects",
    "",
  ];

  for (const article of articles) {
    const { id, slug } = article;
    if (id == null || !slug) {
      throw new Error(`Invalid article entry (need id + slug): ${JSON.stringify(article)}`);
    }

    const target = canonicalPath(id, slug);
    lines.push(`/articles/${id} ${target} 301`);
  }

  lines.push("");
  fs.writeFileSync(OUTPUT, lines.join("\n"), "utf8");
  console.log(`Wrote ${articles.length} redirect(s) to src/_redirects`);
}

main();
